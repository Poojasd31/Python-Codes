# 2. Slicing
# To Fetch multiple elements
# name="rameshwaram"

# SYNTAX: var_name[start index : end index : step value]

# start_index: Slicing start from start index

# end_Index: Slicing will stop before end index

# Step Value: (It is like stair case)
# type: ignore


name2="rameshwaram"
print(name2[0:3:1]) 
print(name2[2:6:1])
print(name2[-9:-5:1])
print(name2[2:-5:1])
print(name2[-1:-4:-1])
print(name2[2:7:2]) #msw
print(name2[-6:0:-2]) #hea
print(name2[3:-2:1])